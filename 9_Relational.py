# Relational Operator 

num1 = 100
num2 =200

ans = num1 == num2
print(f"{ans} = {num1} == {num2}")  

ans = num1 < num2
print(f"{ans} = {num1} < {num2}")  

ans = num1 > num2
print(f"{ans} = {num1} > {num2}")  

ans = num1 <= num2
print(f"{ans} = {num1} <= {num2}")  

ans = num1 >= num2
print(f"{ans} = {num1} >= {num2}")  